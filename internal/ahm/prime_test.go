func TestPrimeInCommittedModeRegeneratesStaleIndexes(t *testing.T) {
	root := newCommittedModeRepoWithMultipleTasks(t, 2)

	// Run prime first to generate indexes.
	if _, stderr, code := runCLI(t, "--root", root, "prime"); code != 0 {
		t.Fatalf("first prime failed: %s", stderr)
	}

	taskIndex := filepath.Join(root, ".ahm", "tasks", "active", "index.md")
	if _, err := os.Stat(taskIndex); err != nil {
		t.Fatalf("task index not generated: %v", err)
	}

	// Stale the generated index.
	if err := os.WriteFile(taskIndex, []byte("# Stale Index\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	// prime should regenerate the stale index.
	if _, stderr, code := runCLI(t, "--root", root, "prime"); code != 0 {
		t.Fatalf("prime after stale index failed: %s", stderr)
	}
	assertFileContainsAll(t, taskIndex, "# Active Tasks", "001")
}

func TestPrimeInCommittedModePreservesGitState(t *testing.T) {
	root := newCommittedModeRepoWithMultipleTasks(t, 1)
	headBefore := strings.TrimSpace(git(t, root, "rev-parse", "HEAD"))
	configFile := filepath.Join(root, ".ahm", "config.json")
	configBefore := mustRead(t, configFile)

	if _, stderr, code := runCLI(t, "--root", root, "prime"); code != 0 {
		t.Fatalf("prime failed: %s", stderr)
	}

	// HEAD unchanged.
	if got := strings.TrimSpace(git(t, root, "rev-parse", "HEAD")); got != headBefore {
		t.Fatalf("prime moved HEAD from %s to %s", headBefore, got)
	}
	// Nothing staged.
	if staged := strings.TrimSpace(git(t, root, "diff", "--cached", "--name-only")); staged != "" {
		t.Fatalf("prime staged changes:\n%s", staged)
	}
	// Config file unchanged (byte-identical before and after prime).
	if got := mustRead(t, configFile); got != configBefore {
		t.Fatalf("prime modified config.json: before=%q after=%q", configBefore, got)
	}
	assertNotContains(t, configBefore, "records_last_sync")
	assertNotContains(t, mustRead(t, configFile), "records_last_sync")
}


func TestBranchCheckoutRegeneratesIndexes(t *testing.T) {
	// Start with a committed-mode repo on main.
	root := newCommittedModeRepoWithMultipleTasks(t, 2)

	// Create a branch with different tasks.
	git(t, root, "checkout", "-q", "-b", "feature-a")
	// Add a third task on this branch.
	writeTaskFile(t, filepath.Join(root, ".ahm", "tasks", "active", "003.md"), "003", "Feature A Work", "Pending", "depends_on: -\n")
	git(t, root, "add", ".ahm/tasks/active/003.md")
	git(t, root, "commit", "-q", "-m", "add feature-a task")

	// Go back to main, run prime, verify index shows only 2 tasks.
	git(t, root, "checkout", "-q", "main")
	// Run prime to regenerate indexes.
	if _, stderr, code := runCLI(t, "--root", root, "prime"); code != 0 {
		t.Fatalf("prime on main failed: %s", stderr)
	}
	taskIndex := filepath.Join(root, ".ahm", "tasks", "active", "index.md")
	assertFileContainsAll(t, taskIndex, "# Active Tasks")
	assertFileContainsAll(t, taskIndex, "001")
	assertFileContainsAll(t, taskIndex, "002")
	assertNotContains(t, mustRead(t, taskIndex), "003")

	// Switch to feature-a, run prime, verify index now shows 3 tasks with 003.
	git(t, root, "checkout", "-q", "feature-a")
	// Stale the index that prime will regenerate.
	writeFile(t, taskIndex, "# Stale\n")
	if _, stderr, code := runCLI(t, "--root", root, "prime"); code != 0 {
		t.Fatalf("prime on feature-a failed: %s", stderr)
	}
	assertFileContainsAll(t, taskIndex, "003")
}

// primeTestRoots is a testing helper that sets up a committed-mode repo for prime tests.
func newCommittedModeRepoWithMultipleTasks(t *testing.T, taskCount int) string {
	t.Helper()
	root := newGitRepo(t)
	writeAHMConfig(t, root)
	writeFile(t, filepath.Join(root, ".ahm", ".gitignore"),
		recordsGitignoreHeader+strings.Join(recordsGitignoreEntries, "\n")+"\n")
	for i := 1; i <= taskCount; i++ {
		id := fmt.Sprintf("%03d", i)
		writeTaskFile(t, filepath.Join(root, ".ahm", "tasks", "active", id+".md"), id, "Task "+id, "Pending", "depends_on: -\n")
	}
	// docs/adr/index.md stays committed (project-facing documentation).
	// Content must match what renderADRIndex([]ADR{}) produces so prime
	// does not rewrite it as a modified tracked file.
	writeFile(t, filepath.Join(root, "docs", "adr", "index.md"), "# ADR Index\n\nThis file is generated by `ahm index`. Do not edit it by hand.\n\n| ADR | Title | Status | Date |\n| --- | ----- | ------ | ---- |\n")
	git(t, root, "add", ".ahm", "docs")
	git(t, root, "commit", "-q", "-m", "add committed workflow records")
	return root
}

